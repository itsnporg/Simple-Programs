//uses of the arithmetic operator//
#include <stdio.h>
int main()
{
    int a=6,b=3,add,sub,multi,division,reminder;
    add=a+b;
    sub=a-b;
    multi=a*b;
    division=a/b;
    reminder=a%b;
    printf("addition:%d\nsubtraction:%d\ndivision:%d\nmultiplication:%d\nremind:%d",add,sub,division,multi,reminder);
    return 0;
}
